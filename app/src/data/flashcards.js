// Helper function to get cards by difficulty
export const getCardsByDifficulty = (difficulty) => {
  return flashcards.filter(card => card.difficulty === difficulty);
};

// Helper function to get cards by tag
export const getCardsByTag = (tag) => {
  return flashcards.filter(card => card.tags.includes(tag));
};

// Helper function to get cards by case
export const getCardsByCase = (caseName) => {
  const caseMap = {
    'nominativ': 'nominativ',
    'akkusativ': 'akkusativ',
    'dativ': 'dativ',
    'genitiv': 'genitiv'
  };
  return flashcards.filter(card => card.tags.includes(caseMap[caseName]));
};

